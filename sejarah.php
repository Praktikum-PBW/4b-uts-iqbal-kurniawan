      <h5>2. Robusta Menggantikan Arabika sebagai Komoditas Utama</h5><br>Tahun 1876, hama Karat Daun menyerang hampir seluruh perkebunan kopi di Indonesia. Belanda kemudian mendatangkan jenis kopi lain, yaitu liberika. Namun, nasibnya sama, habis diserang karat daun.

      Serangan hama tidak membuat Belanda kehilangan akal. Pada tahun 1900, mereka mendatangkan jenis kopi robusta yang lebih mudah perawatannya serta lebih tahan terhadap hama. Produksinya yang sangat tinggi membuat Indonesia sempat menjadi ladang pengekspor terbesar di dunia.
      <h5><br>3. Kebangkitan Perkebunan Kopi Indonesia</h5><br>Pascakemerdekaan, setelah pemerintah Hindia Belanda meninggalkan Indonesia, laju perkebunan kopi pun sedikit terhambat. Namun, berkat komitmen serta kegigihan para petani dan nasionalisasi perkebunan eks pemerintahan Hindia Belanda, akhirnya perkebunan kopi lambat laun mulai bangkit dan berkembang.

      Setidaknya ada satu novel karya Douwes Dekker berjudul Max Havelaar yang membantu mengubah opini masyarakat tentang Sistem Tanam Paksa. Novel tersebut berkisah tentang seorang pedagang kopi dan sekaligus kritik terhadap kesewenang-wenangan pemerintahan Hindia Belanda terhadap rakyat. Oleh karena peran novel itu, maka ada salah satu produk coffee blend dari Indonesia yang menggunakan kata Havelaar sebagai nama produknya.

      Tahun 2000-an, kopi Indonesia kembali melejit. Indonesia masuk dalam negara penghasil kopi terbesar keempat di dunia setelah Brazil, Vietnam, dan Kolombia. Keanekaragaman cita rasa kopi yang tumbuh di berbagai daerah di Indonesia diakui oleh mancanegara.</p>
